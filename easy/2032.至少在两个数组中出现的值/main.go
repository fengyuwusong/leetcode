func twoOutOfThree(nums1 []int, nums2 []int, nums3 []int) []int {
	// todo å¯é€šè¿‡ä½è¿ç®—ä¼˜åŒ–
	memo := make(map[int]string)
	for _, i := range nums1 {
		memo[i] = "1"
	}
	for _, i := range nums2 {
		if memo[i] == "" {
			memo[i] = "01"
		} else if memo[i] == "1" {
			memo[i] = "11"
		}
	}

	for _, i := range nums3 {
		if memo[i] == "" {
			memo[i] = "001"
		} else if len(memo[i]) == 2 {
			memo[i] = memo[i] + "1"
		} else if len(memo[i]) == 1 {
			memo[i] = memo[i] + "01"
		}
	}
	var ans []int
	for k, v := range memo {
		if strings.Count(v, "1") >= 2 {
			ans = append(ans, k)
		}
	}
	return ans
}

