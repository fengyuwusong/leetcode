func check(nums []int) bool {
	flag := 0
	for i := 1; i < len(nums); i++ {
		if flag > 1 {
			return false
		}
		if nums[i] < nums[i-1] {
			flag++
		}
	}
	return flag == 0 || flag == 1 && nums[0] >= nums[len(nums)-1]
}

